  void String_coord (pt, x, y)

    rdsrec_list *pt;
    long *x, *y;
  {
    *x = pt->X + pt->DX / 2 
         - (STRLEN(pt->NAME) * CW) / (2 * scale);
    *y = pt->Y + pt->DY / 2 + CH / (2 * scale);
  }

